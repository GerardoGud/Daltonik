    private void revisar(){
        String FakeUser="FakeUser";
        String pswd="notExist";
        String user=this.tUsuario.getText();
        String pwd=String.copyValueOf(tpContrasena.getPassword());
        boolean b;
        b = ConnectURL.Login(user, pwd);
        if (b) {
            tUsuario.setBackground(new java.awt.Color(51,255,153));
            tpContrasena.setBackground(new java.awt.Color(51,255,153));
//            JOptionPane.showMessageDialog(this, "Login correcto\n Por favor espere un momento", "Login", JOptionPane.INFORMATION_MESSAGE);
            Principal lg;
            if(b){
                lg= new Principal(user, pwd,fondo);
            }else{
                lg= new Principal();
            }
            lg.setVisible(true);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Login incorrecto\n Por favor introduzca sus credenciales nuevamente", "Login error", JOptionPane.ERROR_MESSAGE);
            tUsuario.setBackground(new java.awt.Color(255, 102, 102));
            tpContrasena.setBackground(new java.awt.Color(255, 102, 102));
        }
    }
    
