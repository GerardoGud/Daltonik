    private void bBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBuscarActionPerformed
        // TODO add your handling code here:
        this.tDatos.setModel(cdao.buscarId(tDatos, Integer.parseInt(this.tBusqueda.getText())));
        Limpiar();
    }//GEN-LAST:event_bBuscarActionPerformed

    private void bEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEditarActionPerformed
        // TODO add your handling code here:
        if (edit) {
            edit=false;
            cat = new Categorias();
            cat.setNombre(this.tNombre.getText());
            cat.setIdCategoria(Integer.parseInt(this.tBusqueda.getText()));
            if (this.chkEstatus.isSelected()) {
                cat.setEstatus("A");
            } else {
                cat.setEstatus("I");
            }
            cdao.editarCategorias(cat, cat.getIdCategoria());
            cargar();
            Limpiar();
        } else {
            edit = true;
            this.tDatos.setModel(cdao.buscarId(tDatos, Integer.parseInt(this.tBusqueda.getText())));
            cat = cdao.buscarIdEdicion(Integer.parseInt(this.tBusqueda.getText()));
            this.tNombre.setText(cat.getNombre());
            if (cat.getEstatus().equals("A")) {
                this.chkEstatus.setSelected(true);
            } else {
                this.chkEstatus.setSelected(false);
            }
        }
        this.bBuscar.setVisible(!edit);
        this.bEliminar.setVisible(!edit);
        this.bGuardar.setVisible(!edit);
    }//GEN-LAST:event_bEditarActionPerformed

    private void bEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEliminarActionPerformed
        // TODO add your handling code here:
        cdao.eliminarCategorias(Integer.parseInt(this.tBusqueda.getText()));
        this.tDatos.setModel(cdao.cargarTabla(tDatos, pagina));
        noPaginas=cdao.cantPaginas();
        paginar();
        Limpiar();
    }//GEN-LAST:event_bEliminarActionPerformed

            
    private void bSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSiguienteActionPerformed
        // TODO add your handling code here:
        if(pagina<noPaginas)pagina++;
        paginar();
        cargar();
    }//GEN-LAST:event_bSiguienteActionPerformed

    private void bAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAtrasActionPerformed
        // TODO add your handling code here:
        if(pagina>0)pagina--;
        paginar();
        cargar();
    }//GEN-LAST:event_bAtrasActionPerformed

    private void tBusquedaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tBusquedaKeyTyped
        // TODO add your handling code here:
         char validar = evt.getKeyChar();
        if(Character.isLetter(validar)){
            getToolkit().beep();
            evt.consume();
            
            JOptionPane.showMessageDialog(tBusqueda, "Caracter no valido");
        }
    }//GEN-LAST:event_tBusquedaKeyTyped
    public void cargar() {
        this.tDatos.setModel(cdao.cargarTabla(tDatos,pagina));
    }

    public void Limpiar() {
        this.tNombre.setText("");
        this.tBusqueda.setText("");
    }
    
    public void paginar(){
        if(pagina==0){
            this.bAtras.setVisible(false);
        }else{
            this.bAtras.setVisible(true);
        }
        if(pagina<noPaginas){
            this.bSiguiente.setVisible(true);
        }else{
            this.bSiguiente.setVisible(false);
        }
        this.tNumPage.setText((pagina+1)+" de "+(noPaginas+1));
    }
