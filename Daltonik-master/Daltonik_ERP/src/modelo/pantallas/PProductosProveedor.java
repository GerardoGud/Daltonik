    public void cargar() {
        this.tDatos.setModel(pdao.cargarTabla(tDatos, pagina));
        reg=new ArrayList();
        reg=pdao.buscarProveedores();
        cProveedor.removeAllItems();
        for (int i = 0; i < reg.size(); i++) {
            this.cProveedor.addItem(reg.get(i).getNombre());
        }
        regP=new ArrayList();
        regP=pdao.consultarPres();
        cPresentacion.removeAllItems();
        for (int i = 0; i < regP.size(); i++) {
            this.cPresentacion.addItem(regP.get(i).getNombre());
        }
        this.repaint();
    }
    private int buscarPr(int id){
    int idx=-1;
    for (int i = 0; i < reg.size(); i++) {
        if(reg.get(i).getIdProveedor()==id)return i;
    }
    return idx;
}
private int buscarPp(int id){
    int idx=-1;
    for (int i = 0; i < regP.size(); i++) {
        if(regP.get(i).getId()==id)return i;
    }
    return idx;
}
    public void limpiar(){
        this.tDiasRetardo.setText("");
    }
    
    public void paginar(){
        if(pagina==0){
            this.bAtras.setVisible(false);
        }else{
            this.bAtras.setVisible(true);
        }
        if(pagina<noPaginas){
            this.bSiguiente.setVisible(true);
        }else{
            this.bSiguiente.setVisible(false);
        }
        this.tNumPage.setText((pagina+1)+" de "+(noPaginas+1));
    }

