    private void bEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEditarActionPerformed
        // TODO add your handling code here:
        if(edit) {
            edit=false;
           pro=new ProductosProveedor();
             pro.setIdProveedor(reg.get(proSelected).getIdProveedor());
            pro.setIdPresentacion(regP.get(PsSelected).getId());
            pro.setDiasRetardo(Integer.parseInt(this.tDiasRetardo.getText()));
            pro.setPrecioEstandar(Double.parseDouble(this.tPrecioEstandar.getText()));
            pro.setPrecioUltimaCompra(Double.parseDouble(this.tPrecioUltimaCompra.getText()));
            pro.setCantMinPedir(Integer.parseInt(this.tcantMinPedir.getText()));
            pro.setCantMaxPedir(Integer.parseInt(this.tcantMaxPedir.getText()));
            if(this.chkEstatus.isSelected()){
                pro.setEstatus("A");
            }else{
                pro.setEstatus("I");
            }
            pdao.editarProductosProveedor(pro,pro.getIdProveedor(), pro.getIdPresentacion());
            cargar();
            limpiar();
        }else{
            edit=true;
            this.tDatos.setModel(pdao.buscarId(tDatos, Integer.parseInt(this.tBusqueda.getText())));
            pro = pdao.buscarIdEdicion(Integer.parseInt(this.tBusqueda.getText()));
            this.cProveedor.setSelectedIndex(this.buscarPr(pro.getIdProveedor()));
            this.cPresentacion.setSelectedIndex(this.buscarPp(pro.getIdPresentacion()));
            this.tDiasRetardo.setText(pro.getDiasRetardo()+"");
            this.tPrecioEstandar.setText(pro.getPrecioEstandar()+"");
            this.tPrecioUltimaCompra.setText(pro.getPrecioUltimaCompra()+"");
            this.tcantMinPedir.setText(pro.getCantMinPedir()+"");
            this.tcantMaxPedir.setText(pro.getCantMaxPedir()+"");
            if (pro.getEstatus().equals("A")) {
                this.chkEstatus.setSelected(true);
            } else {
                this.chkEstatus.setSelected(false);
            }
        }
        this.bBuscar.setVisible(!edit);
        this.bEliminar.setVisible(!edit);
        this.bGuardar.setVisible(!edit);
    }//GEN-LAST:event_bEditarActionPerformed

