    private void bEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEditarActionPerformed
        // TODO add your handling code here:
        if (edit) {
            edit=false;
            tu = new TipoUsuario();
            tu.setNombre(this.tNombre.getText());
            tu.setIdTipoUsuario(Integer.parseInt(this.tBusqueda.getText()));
            if (this.chkEstatus.isSelected()) {
                tu.setEstatus("A");
            } else {
                tu.setEstatus("I");
            }
            tudao.editarTipoUsuario(tu, tu.getIdTipoUsuario());
            cargar();
        } else {
            edit = true;
            this.tDatos.setModel(tudao.buscarId(tDatos, Integer.parseInt(this.tBusqueda.getText())));
            tu = tudao.buscarIdEdicion(Integer.parseInt(this.tBusqueda.getText()));
            this.tNombre.setText(tu.getNombre());
            this.tNombre.setText(String.valueOf(tu.getIdTipoUsuario()));
            if (tu.getEstatus().equals("A")) {
                this.chkEstatus.setSelected(true);
            } else {
                this.chkEstatus.setSelected(false);
            }
        }
        this.bBuscar.setVisible(!edit);
        this.bEliminar.setVisible(!edit);
        this.bGuardar.setVisible(!edit);
    }//GEN-LAST:event_bEditarActionPerformed

    private void bEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEliminarActionPerformed
        // TODO add your handling code here:
        tudao.eliminarTipoUsuario(Integer.parseInt(this.tBusqueda.getText()));
        cargar();
        Limpiar();
    }//GEN-LAST:event_bEliminarActionPerformed

    private void bGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGuardarActionPerformed
        tu = new TipoUsuario();
        tu.setNombre(this.tNombre.getText());
        tu.setIdTipoUsuario(Integer.parseInt(this.tNombre.getText()));
        if (this.chkEstatus.isSelected()) {
           tu.setEstatus("A");
        } else {
            tu.setEstatus("I");
        }
        tudao.guardarTipoUsuario(tu);
        cargar();
    }//GEN-LAST:event_bGuardarActionPerformed

public void cargar() {
        this.tDatos.setModel(tudao.cargarTabla(tDatos));
    }

    public void Limpiar() {
        this.tIdTipoUsuario.setText("");
        this.tNombre.setText("");
    }
    
