    public void cargar() {
        this.tDatos.setModel(cpdao.cargarTabla(tDatos, pagina));
    }
    public void limpiar() throws SQLException{
        this.tTelefono.setText("");
        this.tNombre.setText("");
        this.tIdContacto.setText(""+cpdao.UltimoID());
        this.tEmail.setText("");
        this.tBusq.setText("");
    }
    public void paginar(){
        if(pagina==0){
            this.bAtras.setVisible(false);
        }else{
            this.bAtras.setVisible(true);
        }
        if(pagina<noPaginas){
            this.bSiguiente.setVisible(true);
        }else{
            this.bSiguiente.setVisible(false);
        }
        this.tNumPage.setText((pagina+1)+" de "+(noPaginas+1));
    }
