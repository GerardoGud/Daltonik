    private void bGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGuardarActionPerformed
            boolean campos = false;
            boolean vTel = false;
             boolean vEmail = false;
            boolean vEmail2 = false;
            if(this.tIdContacto.getText().length() == 0 || this.tNombre.getText().length() == 0 || this.tEmail.getText().length() == 0 || this.tTelefono.getText().length() == 0){
                JOptionPane.showMessageDialog(tNombre, "No puede haber campos vacios"); 
            }
            else{
                campos = true;
                if(this.tTelefono.getText().length() != 10){
                JOptionPane.showMessageDialog(tTelefono, "El numero de telefono no es valido");
            }
            else{
                vTel = true;
            }
           
            if(this.tEmail.getText().contains("@")){
                vEmail = true;
            }
            else{
                JOptionPane.showMessageDialog(tEmail, "La dirección de Email no es válida");
            }
            if(this.tEmail.getText().contains(".")){
                vEmail2 = true;
            }
            else{
                JOptionPane.showMessageDialog(tEmail, "La dirección de Email no es válida");
            }
            }
            
            
    
        if(campos == true && vTel == true && vEmail == true && vEmail2 == true){
            cpro=new ContactosProveedor();
        cpro.setIdContacto(Integer.parseInt(this.tIdContacto.getText()));
        cpro.setNombre(this.tNombre.getText());
        cpro.setTelefono(this.tTelefono.getText());
        cpro.setEmail(this.tEmail.getText());
        cpro.setIdProveedor(this.comboProv.getSelectedIndex()+1);
        if(this.chkEstatus.isSelected()){
            cpro.setEstatus("A");
        }else{
            cpro.setEstatus("I");
        }
        cpdao.guardarContacto(cpro);
        noPaginas=cpdao.cantPaginas();
        cargar();
        paginar();
                try {
                    limpiar();
                } catch (SQLException ex) {
                    Logger.getLogger(PContProveedor.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
        try {
            tIdContacto.setText(""+cpdao.UltimoID());
        } catch (SQLException ex) {
            Logger.getLogger(PContProveedor.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bGuardarActionPerformed

