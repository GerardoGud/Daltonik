    private void bBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bBuscarActionPerformed
       
        if (this.bBusqTexto2.isSelected() == false) {
            this.tDatos.setModel(cpdao.buscarId(tDatos, this.comboBusqueda.getSelectedIndex()+1));
            try {
                limpiar();
            } catch (SQLException ex) {
                Logger.getLogger(PContProveedor.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            this.tDatos.setModel(cpdao.buscarCadena(tDatos, this.tBusq.getText()));
            try {
                limpiar();
            } catch (SQLException ex) {
                Logger.getLogger(PContProveedor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_bBuscarActionPerformed

    private void bEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEditarActionPerformed
        // TODO add your handling code here:
        if(edit) {
            edit=false;
            cpro = new ContactosProveedor();
        cpro.setIdContacto(Integer.parseInt(this.tIdContacto.getText()));
        cpro.setNombre(this.tNombre.getText());
        cpro.setTelefono(this.tTelefono.getText());
        cpro.setEmail(this.tEmail.getText());
        cpro.setIdProveedor(this.comboProv.getSelectedIndex()+1);
        if(this.chkEstatus.isSelected()){
            cpro.setEstatus("A");
        }else{
            cpro.setEstatus("I");
        }
            cpdao.editarContacto(cpro, cpro.getIdContacto());
            cargar();
            paginar();
            try {
                limpiar();
            } catch (SQLException ex) {
                Logger.getLogger(PContProveedor.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            
            edit=true;
            this.tDatos.setModel(cpdao.buscarId(tDatos, this.comboBusqueda.getSelectedIndex()+1));
            cpro = cpdao.buscarIdEdicion(this.comboBusqueda.getSelectedIndex()+1);
            this.tIdContacto.setText(Integer.toString(cpro.getIdContacto()));
            this.tNombre.setText(cpro.getNombre());
            this.tTelefono.setText(cpro.getTelefono());
            this.tEmail.setText(cpro.getEmail());
            this.comboProv.setSelectedIndex(cpro.getIdProveedor());
            if (cpro.getEstatus().equals("A")) {
                this.chkEstatus.setSelected(true);
            } else {
                this.chkEstatus.setSelected(false);
            }  
        }
        this.bBuscar.setVisible(!edit);
        this.bEliminar.setVisible(!edit);
        this.bGuardar.setVisible(!edit);
    }//GEN-LAST:event_bEditarActionPerformed

